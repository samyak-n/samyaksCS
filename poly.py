def poly_add2(p1, p2):
    p3 = [0, 0, 0]
    p3[0] = p1[0] + p2[0]
    p3[1] = p1[1] + p2[1]
    p3[2] = p1[2] + p2[2]
    return p3


def poly_mult2(p1, p2):
    p31 = [0, 0, 0, 0, 0]
    p32 = [0, 0, 0, 0, 0]
    p33 = [0, 0, 0, 0, 0]
    p34 = [0, 0, 0, 0, 0]
    p31[0] = p1[0] * p2[0]
    p31[1] = p1[0] * p2[1]
    p31[2] = p1[0] * p2[2]
    p32[1] = p1[1] * p2[0]
    p32[2] = p1[1] * p2[1]
    p32[3] = p1[1] * p2[2]
    p33[2] = p1[2] * p2[0]
    p33[3] = p1[2] * p2[1]
    p33[4] = p1[2] * p2[2]
    p34[0] = p31[0] + p32[0] + p33[0]
    p34[1] = p31[1] + p32[1] + p33[1]
    p34[2] = p31[2] + p32[2] + p33[2]
    p34[3] = p31[3] + p32[3] + p33[3]
    p34[4] = p31[4] + p32[4] + p33[4]
    return p34
